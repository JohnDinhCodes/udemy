// Create a function convertFahrenheitToCelsius

function convertFahrenheitToCelsius(fahrenheitDegrees) {
    return (fahrenheitDegrees - 32) * (5 / 9);
}

console.log(convertFahrenheitToCelsius(32));
console.log(convertFahrenheitToCelsius(68));